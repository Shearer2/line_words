    # –í —Å—Ç—Ä–æ–∫—É –¥–ª—è –æ—Ç–≤–µ—Ç–∞ –¥–æ–±–∞–≤–ª—è–µ–º reply_markup=keyboard, —á—Ç–æ–±—ã –ø–æ–∫–∞–∑–∞—Ç—å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –≤ —Ç–µ–ª–µ–≥—Ä–∞–º.
